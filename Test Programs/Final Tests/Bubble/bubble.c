void swap(int* x, int* y) {
    int temp = *x;
    *x = *y;
    *y = temp;
}

void bubble_sort(int* arr, int size) {
    for (int i = 0; i < size - 1; i++) {
        int swapped = 0;
        for (int j = 0; j < size - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                swap(&arr[j], &arr[j + 1]);
                swapped = 1;
            }
        }
        if (swapped == 1)
            break;
    }
}

